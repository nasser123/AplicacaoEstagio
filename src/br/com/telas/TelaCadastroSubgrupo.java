    private void jButtonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGravarActionPerformed

        if (novo) {
            subgrupo1 = new Subgrupo();
            subgrupo1.setDescricao(jTextFieldDescricao.getText());
            subgrupo1.setIdgrupo((Grupo) jComboBoxGrupo.getSelectedItem());
            try {
                subgrupo1 = subGrupoDAO.inserirComRetorno(subgrupo1);
                if (subgrupo1 != null) {
                    novo = false;
                }
                novo = false;
            } catch (SQLException ex) {
                Logger.getLogger(TelaCadastroSubgrupo.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            try {
                subGrupoDAO.alterar(subgrupo1);
            } catch (SQLException ex) {
                Logger.getLogger(TelaCadastroSubgrupo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        for (int i = 0; i < tiposAtributosSelecionados.size(); i++) {
            ListaAtributoSubgrupo las = new ListaAtributoSubgrupo();
            las.setIdsubgrupo(subgrupo1);
            las.setIdtipoAtributo(tiposAtributosSelecionados.get(i));
            try {
                listaAtributoSubgrupoDAO.inserir(las);
            } catch (SQLException ex) {
                Logger.getLogger(TelaCadastroSubgrupo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        JOptionPane.showMessageDialog(rootPane, "Subgrupo atualizado com sucesso", Variaveis.getSiglaSistema(), 1);
    }//GEN-LAST:event_jButtonGravarActionPerformed

