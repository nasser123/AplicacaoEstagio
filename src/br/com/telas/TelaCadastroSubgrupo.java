    private void preencheJListTiposAtributos() {
        tiposAtributosSelecionados = subGrupoDAO.pesquisarTiposAtributos(subgrupo);
        try {
            tipoAtributoList = tipoAtributoDAO.pesquisaTiposAtributosNaoPresentes(tiposAtributosSelecionados);
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastroSubgrupo.class.getName()).log(Level.SEVERE, null, ex);
        }
        atualizaJList();
    }

